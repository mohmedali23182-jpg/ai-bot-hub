def prompt_for_kind(kind: str, caption: str = '') -> str:
    caption = (caption or '').strip()
    prompts = {
        'image': 'أنت خبير تحليل صور. حلل هذه الصورة بدقة واستخرج النصوص والعناصر المهمة بالعربية.',
        'audio': 'أنت خبير تحليل صوتي. حلل هذا الملف الصوتي، فرّغه إن أمكن ثم لخّصه واستخرج أهم النقاط بالعربية.',
        'video': 'أنت خبير تحليل فيديو. حلل هذا الفيديو ولخّص أهم المشاهد والمحتوى الظاهر بالعربية.',
        'document': 'أنت خبير تحليل مستندات. حلل هذا الملف واستخرج أهم المعلومات والبيانات منه بالعربية.',
    }
    base = prompts.get(kind, 'أنت مساعد ذكي. حلل هذا المحتوى وقدم ردًا دقيقًا ومنظمًا بالعربية.')
    return f'{base}\n\nتعليمات المستخدم الإضافية: {caption}' if caption else base


