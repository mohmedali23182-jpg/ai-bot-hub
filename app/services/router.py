from typing import Optional

def prompt_for_kind(kind: str, caption: str = '') -> str:
    caption = (caption or '').strip()
    prompts = {
        'image': 'أنت خبير تحليل صور وبصريات. حلل هذه الصورة بدقة واستخرج النصوص والعناصر المهمة مع تقديم وصف تفصيلي بالعربية.',
        'audio': 'أنت خبير تحليل صوتي. حلل هذا الملف الصوتي، قم بتفريغه نصياً إن أمكن، ثم لخّصه واستخرج أهم النقاط الرئيسية بالعربية.',
        'video': 'أنت خبير تحليل فيديو. حلل هذا الفيديو بدقة ولخّص أهم المشاهد، الأحداث، والمحتوى الظاهر بالعربية.',
        'document': 'أنت خبير تحليل مستندات وبيانات. حلل هذا الملف واستخرج أهم المعلومات، الجداول، أو البيانات البرمجية منه بدقة بالعربية.',
    }
    base = prompts.get(kind, 'أنت مساعد ذكاء اصطناعي محترف. حلل هذا المحتوى وقدم ردًا دقيقًا، منظمًا، ومفيدًا بالعربية.')
    return f'{base}\n\nتعليمات المستخدم الإضافية: {caption}' if caption else base


